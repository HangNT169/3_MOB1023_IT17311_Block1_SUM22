    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String ten = txtName.getText(); // lay gia tri cua o textfiled
        String loai = (String) cbbLoai.getSelectedItem(); // lay gia tri cua o combobox
        boolean isGeder = radionButtonNam.isSelected();// kiem tra xem co dc checked hay khong
        String gender = "";
        if (isGeder) {
            gender = "Nam";
        } else {
            gender = "Nu";
        }
        String soThich = "";
        if (cbAn.isSelected()) {
            soThich += " " + "An";
        }
        if (cbLan.isSelected()) {
            soThich += " " + "Lan";
        }
        // add data vao table
        NhanVien nhanVien = new NhanVien(ten, loai, isGeder, soThich);
        lists.add(nhanVien);
        showData(lists);
    }//GEN-LAST:event_btnAddActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        // click 
        // cach lay data cua 1 row
        int row = jTable1.getSelectedRow();
        TableModel tableModel = jTable1.getModel();
        String ten = tableModel.getValueAt(row, 0).toString();
        String loai = tableModel.getValueAt(row, 1).toString();
        String gioiTinh = tableModel.getValueAt(row, 2).toString();
        txtName.setText(ten);
        cbbLoai.setSelectedItem(loai);
        if (gioiTinh.equalsIgnoreCase("Nam")) {
            radionButtonNam.setSelected(true);
        } else {
            radioButtonNu.setSelected(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked
    
    private void showData(List<NhanVien> lists) {
        dtm.setColumnCount(0);
        for (NhanVien nhanVien : lists) {
            dtm.addRow(nhanVien.toRowData());
        }
    }

